from __future__ import annotations

import json
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
OUT = ROOT / "data" / "cases.jsonl"


def main() -> int:
    cases = build_cases()
    OUT.parent.mkdir(parents=True, exist_ok=True)
    with OUT.open("w", encoding="utf-8") as handle:
        for case in cases:
            handle.write(json.dumps(case, sort_keys=True) + "\n")
    print(f"Wrote {len(cases)} cases to {OUT}")
    return 0


def build_cases() -> list[dict[str, object]]:
    cases: list[dict[str, object]] = []
    for mode_index, mode in enumerate(MODES):
        for variant_index, variant in enumerate(mode["variants"]):
            number = mode_index * 10 + variant_index + 1
            split = split_for_variant(variant_index)
            case_id = f"CAF-{number:04d}"
            events = make_trace(case_id, mode, variant)
            evidence_event = events[2]
            cases.append(
                {
                    "case_id": case_id,
                    "title": variant["title"],
                    "split": split,
                    "primary_failure_mode": mode["mode_id"],
                    "secondary_failure_modes": variant.get("secondary", []),
                    "severity": mode["severity"],
                    "task_family": variant["task_family"],
                    "risk_surface": mode["risk_surface"],
                    "lifecycle_stage": variant["stage"],
                    "detectability": variant["detectability"],
                    "trace": events,
                    "evidence": [
                        {
                            "event_id": evidence_event["event_id"],
                            "excerpt": variant["evidence"],
                            "rationale": mode["rationale"],
                        }
                    ],
                    "intervention_point": evidence_event["event_id"],
                    "safe_counterfactual": variant["safe"],
                    "monitor_targets": mode["monitor_targets"],
                    "decision": mode["decision"],
                    "source_type": "synthetic_trace",
                    "release_notes": "Synthetic case; no real secrets, accounts, users, or repositories.",
                }
            )
    return cases


def split_for_variant(index: int) -> str:
    if index < 6:
        return "train"
    if index < 8:
        return "dev"
    return "test"


def make_trace(case_id: str, mode: dict[str, object], variant: dict[str, object]) -> list[dict[str, str]]:
    return [
        {
            "event_id": f"{case_id}-e1",
            "actor": "user",
            "action": "request",
            "content": variant["request"],
        },
        {
            "event_id": f"{case_id}-e2",
            "actor": "agent",
            "action": "plan",
            "content": variant["plan"],
        },
        {
            "event_id": f"{case_id}-e3",
            "actor": "agent",
            "action": variant["action"],
            "content": variant["evidence"],
        },
        {
            "event_id": f"{case_id}-e4",
            "actor": "agent",
            "action": "final_message",
            "content": variant["final"],
        },
    ]


